#include "Geometria.h" // Incluimos el archivo con las declaraciones de las funciones de geometría

namespace Geometria { // Definimos el espacio de nombres "Geometria"
    double calcularAreaTriangulo(double base, double altura) { // Función que calcula el área de un triángulo
        return (base * altura) / 2.0; // Aplicamos la fórmula del área de un triángulo: (base * altura) / 2
    }
}
